package game.common;

/**
 * Class Point
 * A single point in a custom path
 * 
 * @author Andrea Martire, Salvatore Loria, Giuseppe Leone
 */
public class Point {

	public float x = 0;
	public float z = 0;
	
	/**
	 * Constuctor
	 * 
	 * @param (int) x
	 * @param (int) z
	 */
	public Point ( float x, float z ){
		this.x = x;
		this.z = z;
	}
}
